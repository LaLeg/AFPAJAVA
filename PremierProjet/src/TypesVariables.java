
public class TypesVariables {

	public static void main(String[] args) {
		//int
		int entier = 14 ;
		System.out.println("la variable entier = " + entier);
		
		//float
		float decimal = 12.5f;
		System.out.println("la variable decimal = " + decimal);
		
		//double
		double decimalDoublePrec = 15.5 ;
		System.out.println("la variable decimalDoublePrec = " + decimalDoublePrec);
		
		// boolean
		boolean bool = true;
		System.out.println("la variable bool = " + bool);
		
		//char
		char caractere = 'Z';
		System.out.println("la variable caractere = " + caractere);
		
		//string
		String chaine = "Hello";
		System.out.println("la variable chaine = " + chaine);
		
		// opération avec deux variables
		
		// entier 1
		int entier1 = 10 ;
		
		// entier 2
		int entier2 = 20 ;
		
		// affichage de l'opération produit entier1 par entier2
		
		System.out.println("le produit = " + entier1 * entier2);
		System.out.println("le produit entier1 * entier2 = " + entier1 * entier2);
		System.out.println("entier1 = " + entier1 );
		System.out.println("entier2 = " + entier2 );
		System.out.println("le produit entier1 * entier2 = " + entier1 * entier2);
		
		// note avec coefficient
		double note = 13.6;
		int coef = 2;
		System.out.println(" Note finale = note * coef = " + note * coef);
		System.out.println("Note finale = note * coef = " + note * coef);
		
		// Déclaration et affectation
		// Type nomVariable = valeur ;
		int en = 12;
		double dec = 15.5;
		char car = 'a';
		boolean bo = false;
		String ch = "chaine";
		
		// Affectation
		// nomVariable =  valeur ;
		en = 100;
		dec = 1.5;
		car = 'b';
		bo = true;
		ch = "ok";
		System.out.println("en = " + en);
		System.out.println("dec = " + dec);
		System.out.println("car = " + car);
		System.out.println("bo = " + bo);
		System.out.println("ch = " + ch);
		
		
		
		
		
		
		
		// TODO Auto-generated method stub

	}

}
