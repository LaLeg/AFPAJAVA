
public class premiereClasse {

	public static void main(String[] args) {
		// afficher une chaine de caractères
		System.out.println("Bonjour");
		System.out.println("Bonsoir");
		
		// afficher un nombre entier
		System.out.println("100");
		// afficher un nombre avec une ,
		System.out.println("100,123");
		// afficher un caractère et non une chaine de caractère
		System.out.println("&");
		// string -> chaine de caractères
		// int -> entier
		// char -> caractère
		// boolean -> true ou false
		System.out.println(true);
		// Résultat d'un calcul
		System.out.println(10*10);
		System.out.println(10+10);
		// Afficher plusieures données
		System.out.println("Bonjour" + "Monsieur");
		System.out.println("Bonjour " + "Monsieur");
		System.out.println("Bonjour " + "Monsieur " + 100);
		System.out.println("10*10 = " + (10*10));
		System.out.println ();
		// System.out.print
		System.out.print("Bonjour");
		System.out.print("Bonsoir");
		
		
	}

}
